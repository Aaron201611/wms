package com.yunkouan.wms.modules.assistance.vo;

public class MessageHead {
	private String MESSAGE_ID;
	private String MESSAGE_TYPE;
	private String MESSAGE_TIME;
	private String MESSAGE_SOURCE;
	private String MESSAGE_DEST;

	public String getMESSAGE_ID() {
		return MESSAGE_ID;
	}
	public void setMESSAGE_ID(String mESSAGE_ID) {
		MESSAGE_ID = mESSAGE_ID;
	}
	public String getMESSAGE_TYPE() {
		return MESSAGE_TYPE;
	}
	public void setMESSAGE_TYPE(String mESSAGE_TYPE) {
		MESSAGE_TYPE = mESSAGE_TYPE;
	}
	public String getMESSAGE_TIME() {
		return MESSAGE_TIME;
	}
	public void setMESSAGE_TIME(String mESSAGE_TIME) {
		MESSAGE_TIME = mESSAGE_TIME;
	}
	public String getMESSAGE_SOURCE() {
		return MESSAGE_SOURCE;
	}
	public void setMESSAGE_SOURCE(String mESSAGE_SOURCE) {
		MESSAGE_SOURCE = mESSAGE_SOURCE;
	}
	public String getMESSAGE_DEST() {
		return MESSAGE_DEST;
	}
	public void setMESSAGE_DEST(String mESSAGE_DEST) {
		MESSAGE_DEST = mESSAGE_DEST;
	}
}